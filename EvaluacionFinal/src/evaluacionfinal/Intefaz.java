    // Método para actualizar la JTable con los registros de Empresa
    private void actualizarTabla() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Limpiar la tabla antes de mostrar los registros

        // Obtener los registros y agregarlos a la JTable
        for (String[] registro : Empresa.obtenerRegistros()) {
            model.addRow(registro);
        }
    }
    
